package com.syntaxJava07;

public class IncrementAndDecrement {

	public static void main(String[] args) {
		int num=90;
		num=num+1;
		
		System.out.println(num);
		
		num++; //increment ++ //91+1
		System.out.println(num);
		num--;//decrement //91-1
		System.out.println(num);
		
		int i=10;
		i--;
		i--;
		System.out.println(i);
	}

}
