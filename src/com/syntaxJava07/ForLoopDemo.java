package com.syntaxJava07;

public class ForLoopDemo {

	public static void main(String[] args) {
		
		for(int i=1; i<=5; i++) {
			System.out.println("Hello");
		}
		
		System.out.println("_______________________________");
		
		//print numbers from 1 to 20
		for(int i=1; i<=20; i++) {
			System.out.print(i+" ");
		}
		System.out.println("_______________________________");
		
		for(int i=30; i>=10; i--) {
		System.out.print(i+" ");	
		}
	}

}
