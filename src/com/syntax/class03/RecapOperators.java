package com.syntax.class03;

public class RecapOperators {

	public static void main(String[] args) {
		/*
		 * Addition => +
		 * Subtraction => +
		 * Multiplication => *
		 * Division => / 
		 * Modulus => %
		 */
		int a1=10;
		int a2=5;
		System.out.println(a1/a2); //divide a1 by a2
		
		double a3=9;
		double a4=2;
		System.out.println(a3/a4); //divide a1 by a2
		
		float a5= 1.0f;
		float a6 =3.0F;
		System.out.println(a5/a6);
		
		double a7=1;
		double a8=3;
		System.out.println(a7/a8);
		
		


	}

}
